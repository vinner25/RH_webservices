package com.ridehours.domain.model;

public class Vehicle {

	private int vehicle_id;
	
	private int city_id;
	
	private int pickup_center_id;
	
	private String registration_number;
	
	private String chasis_number;
	
	private String color;
	
	private String make;
	
	private String model;
	
	private String rc_number;
	
	private String insurance_validity;
	
	private String vehicle_category_id;

	public int getVehicle_id() {
		return vehicle_id;
	}

	public void setVehicle_id(int vehicle_id) {
		this.vehicle_id = vehicle_id;
	}

	public int getCity_id() {
		return city_id;
	}

	public void setCity_id(int city_id) {
		this.city_id = city_id;
	}

	public int getPickup_center_id() {
		return pickup_center_id;
	}

	public void setPickup_center_id(int pickup_center_id) {
		this.pickup_center_id = pickup_center_id;
	}

	public String getRegistration_number() {
		return registration_number;
	}

	public void setRegistration_number(String registration_number) {
		this.registration_number = registration_number;
	}

	public String getChasis_number() {
		return chasis_number;
	}

	public void setChasis_number(String chasis_number) {
		this.chasis_number = chasis_number;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getMake() {
		return make;
	}

	public void setMake(String make) {
		this.make = make;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getRc_number() {
		return rc_number;
	}

	public void setRc_number(String rc_number) {
		this.rc_number = rc_number;
	}

	public String getInsurance_validity() {
		return insurance_validity;
	}

	public void setInsurance_validity(String insurance_validity) {
		this.insurance_validity = insurance_validity;
	}

	public String getVehicle_category_id() {
		return vehicle_category_id;
	}

	public void setVehicle_category_id(String vehicle_category_id) {
		this.vehicle_category_id = vehicle_category_id;
	}
	
	
	
}
